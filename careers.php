<script>
document.addEventListener('DOMContentLoaded', function() {
    const toggleTitles = document.querySelectorAll('.careers-toggle-title');
    
    toggleTitles.forEach(title => {
        title.addEventListener('click', function() {
            const contentId = this.getAttribute('data-toggle');
            const content = document.getElementById(contentId);
            const icon = this.querySelector('.toggle-icon');
            
            // Close all other open toggles
            document.querySelectorAll('.careers-toggle-content.active').forEach(item => {
                if (item.id !== contentId) {
                    item.classList.remove('active');
                }
            });
            
            document.querySelectorAll('.toggle-icon.active').forEach(icon => {
                if (icon !== this.querySelector('.toggle-icon')) {
                    icon.classList.remove('active');
                }
            });
            
            // Toggle the clicked content
            content.classList.toggle('active');
            icon.classList.toggle('active');
        });
    });
});
</script>

